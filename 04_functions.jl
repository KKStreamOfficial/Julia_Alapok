# the standard form for functions
# function f(x,y)
#   return x+y
#  end
# The one line functions can be write as:
f(x,y)=x+y
# g=f create a g function from f function
g=f
# test it with 1,2
println("f(1,2)=",f(1,2))
println("g(1,2)=",g(1,2))
# You can use Unicode characters
∑(α,β)=α+β
println("∑(1,2)=",∑(1,2))
